#include "libft.h"

void	ft_putnbr_fd(int nb, int fb)
{
	if (nb == -2147483648)
	{
		write (fb, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		ft_putchar_fd('-', fb);
		nb = -nb;
	}
	if (nb > 9)
	{
		ft_putnbr_fd(nb / 10, fb);
	}
	ft_putchar_fd(nb % 10 + '0', fb);
}
